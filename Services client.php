<h2>Quelle est la politique de bagages enregistrés sur GoTravel ?</h2>
<p>Chez GoTravel, un passager a la possibilité d’enregistrer jusqu’à trois bagages, s’il paie les frais applicables pour leur enregistrement à l’aéroport . Il est à noter qu’après une réservation, il est possible, si le besoin s’en fait sentir de rajouter des bagages enregistrés et ce, jusqu’à deux heures avant le départ du vol. Attention, ce n’est pas gratuit ! Si vous voyagez à plusieurs, vous pouvez mettre en commun vos bagages et réaliser ce que l’on appelle un partage d’allocations de bagages enregistrés. Une seule condition : posséder la même réservation !

Grâce à ce partage d’allocation, vous avez le droit de répartir le poids maximum des bagages enregistrés entre les passagers d’une même réservation. Ainsi, un passager pourra prendre un bagage de 30 kg et le second un bagage de 10 kg, si la réservation compte une franchise de bagages de 40 kg. En ce qui concerne la taille bagage sur GoTravel, il ne doit pas excéder les dimensions suivantes : 81 cm x 119 cm x 119 cm. Au niveau du poids, votre bagage enregistré ne doit pas peser plus de 32 kg. Respectez scrupuleusement ces dimensions et ce poids si vous ne voulez pas payer un surcoût ou voir vos bagages refusés pour des raisons de sécurité, lors de l'embarquement.</p>
<h2>Que peut-on mettre dans un bagage cabine dans un avion GoTravel ?</h2>
<p>La compagnie autorise, aujourd'hui, deux bagages en cabine pour les détenteurs d'un billet avec priorité. Le premier de ces bagages est un petit sac à main. L'autre est une petite valise à roulette. La dimension d’un bagage cabine sur GoTravel ne doit pas dépasser 55 cm x 40 cm x 20 cm. Si vous dépassez ces dimensions et un poids maximal de 20 kg, vous prenez le risque de voir votre bagage partir en soute malgré une priorité. Un sac à langer, n’excédant pas 5 kg sera admis en cabine, en plus des bagages à main, pour un voyageur accompagné d’un bébé âgé de moins de deux ans.

Même si votre bagage respecte les règles d’encombrement émises par GoTravel, vous n’aurez pas le droit de transporter tout ce que vous voulez en cabine. Vous veillerez, par exemple, à ne pas transporter d’armes, que votre bagage soit placé en cabine ou même en soute. Vous n’oublierez pas que les aérosols et les liquides au-delà de 100 ml sont interdits en cabine et qu’ils doivent obligatoirement être placés en soute. Quant aux équipements de sport ou aux vélos, ils sont considérés comme des bagages à mettre en soute, doivent donc respecter les règles énoncées plus haut et peuvent même faire l’objet de frais supplémentaires.</p>
